import java.util.*;

public class Star_Pattern {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter value of a");
        int a = sc.nextInt();
        for( int i=0; i<=a; i++);
        for( int j=0; j<=a; j++);
        System.out.println(" * " );


    }
   


}