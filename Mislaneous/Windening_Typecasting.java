public class Windening_Typecasting {
    public static void main(String[] args) {
        int num1;
        byte num2 = 20;

        // We are assigning smaller datatype 
        // byte to larger datatype 
        num1 = num2;

        // Print the output 
        System.out.println("The value of num1 is :" + num1);
    }

}

